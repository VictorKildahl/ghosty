"use client";

import { AuthShell } from "@/app/components/auth-shell";
import { useState } from "react";

export function WelcomeView({
  onContinueWithEmail,
}: {
  onContinueWithEmail: (email: string) => void;
}) {
  const [email, setEmail] = useState("");

  function handleContinue(e: React.FormEvent) {
    e.preventDefault();
    if (email.trim()) {
      onContinueWithEmail(email.trim());
    }
  }

  return (
    <AuthShell
      title="Just ghost write it."
      subtitle="You talk, it writes. That's it."
      currentStep="signin"
    >
      <div className="flex flex-col gap-3">
        {/* Google sign-in */}
        <button
          type="button"
          className="flex items-center justify-center gap-3 rounded-lg border border-border bg-white px-4 py-2.5 text-sm font-medium text-ink shadow-sm transition hover:bg-sidebar"
        >
          <svg className="h-4 w-4" viewBox="0 0 24 24">
            <path
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"
              fill="#4285F4"
            />
            <path
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              fill="#34A853"
            />
            <path
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              fill="#FBBC05"
            />
            <path
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              fill="#EA4335"
            />
          </svg>
          Continue with Google
        </button>

        {/* Divider */}
        <div className="flex items-center gap-3 py-1">
          <div className="h-px flex-1 bg-border" />
          <span className="text-xs text-muted">OR</span>
          <div className="h-px flex-1 bg-border" />
        </div>

        {/* Email entry */}
        <form onSubmit={handleContinue} className="flex flex-col gap-3">
          <input
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            placeholder="Enter an email address"
            required
            autoFocus
            className="rounded-lg border border-border bg-white px-3 py-2.5 text-sm text-ink placeholder:text-muted/60 focus:outline-none focus:ring-2 focus:ring-accent/40"
          />

          <button
            type="submit"
            className="rounded-lg bg-accent px-4 py-2.5 text-sm font-medium text-white transition hover:bg-accent/90"
          >
            Continue with Email
          </button>
        </form>

        <p className="mt-2 text-center text-xs text-ink/80 w-80 mx-auto">
          By signing up, you agree to our{" "}
          <button type="button" className="font-semibold text-ink">
            Terms of Service
          </button>{" "}
          and{" "}
          <button type="button" className="font-semibold text-ink">
            Privacy Policy
          </button>
          .
        </p>
      </div>
    </AuthShell>
  );
}
